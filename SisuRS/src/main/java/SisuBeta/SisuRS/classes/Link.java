package SisuBeta.SisuRS.classes;

public class Link {
    private String href;
    private String rel;
    
    public String getHref() {
        return href;
    }
    public String getRel() {
        return rel;
    }
    public void setHref(String href) {
        this.href = href;
    }
    public void setRel(String rel) {
        this.rel = rel;
    }

}
